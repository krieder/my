package pac;

public class mob {
private int num;
private String name;
public mob(int num, String name){
	this.name = name;
	this.num = num;
}
public int getNum() {
	return num;
}
public void setNum(int num) {
	this.num = num;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
}
